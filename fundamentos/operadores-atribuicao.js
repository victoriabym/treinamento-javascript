const a = 7
let b = 3

// acrescentar - somar
b += a // b = b + a
console.log(b)

// retirar - subtrair
b -= 4 // b = b - 2
console.log(b)

// multiplicar
b *= 2 // b = b * 2
console.log(b)

// dividir
b /= 2 // b = b / 2
console.log(b)

// módulo
b %= 2 // b = b % 2
console.log(b) // resto da divisão
